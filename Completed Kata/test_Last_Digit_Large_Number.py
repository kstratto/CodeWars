from Last_Digit_Large_Number import last_digit

def test_small_ab():
    assert last_digit(4, 1) == 4
    assert last_digit(4, 2) == 6
    assert last_digit(9, 7) == 9
    
def test_exp_0():
    assert last_digit(0, 0) == 1
    for i in range(9):
        a = i**i
        assert last_digit(a, 0) == 1
        
def test_large_a_exp_0():
    assert last_digit(6645391269759572930505079000461097974791497753034689069, 0) == 1
    assert last_digit(5845033340080515300025323035106386058502351459774559381065, 0) == 1
    assert last_digit(902854008175306564917013506950804805839387338193680550715, 0) == 1
    assert last_digit(12400800396849941065283125427580243342995749370819389684631, 0) == 1
    assert last_digit(11804291277221910176104957655504763557895961846370123820697, 0) == 1
    assert last_digit(4463251215468011295383331272610057782304651684663122985959, 0) == 1
    assert last_digit(2721440043624576123996452396732459255434254210365276285290, 0) == 1
    assert last_digit(495046015555024558083915011703273143612069378101740036149, 0) == 1
    assert last_digit(3121024958093965330053073075149725794987250956048925441790, 0) == 1
    
def test_large_values():
    assert last_digit(10, 10 ** 10) == 0
    assert last_digit(2 ** 200, 2 ** 300) == 6
    assert last_digit(3715290469715693021198967285016729344580685479654510946723, 68819615221552997273737174557165657483427362207517952651) == 7
    
